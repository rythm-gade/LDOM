package rit.ldom;

/**
 * Different options for the "face" of text.
 */
public enum Style {

        /**
         * Bold text, indicated in HTML with a "b" tag or a font-weight style
         * parameter
         */
        bold,

        /**
         * Italicized text, indicated in HTML with an "i" tag or a font-style
         * style parameter
         */
        italic,

        /**
         * Underlined text, indicated in HTML with a "u" tag
         */
        underline
}
